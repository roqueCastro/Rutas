package com.example.rutas.entidades;

public class Coordenada {
    String ruta_name;
    String lat;
    String lng;
    String conductor_name;
    String col_lat;
    String col_lng;
    String col_tel;

    public String getRuta_name() {
        return ruta_name;
    }

    public void setRuta_name(String ruta_name) {
        this.ruta_name = ruta_name;
    }

    public String getLat() {
        return lat;
    }

    public void setLat(String lat) {
        this.lat = lat;
    }

    public String getLng() {
        return lng;
    }

    public void setLng(String lng) {
        this.lng = lng;
    }

    public String getConductor_name() {
        return conductor_name;
    }

    public void setConductor_name(String conductor_name) {
        this.conductor_name = conductor_name;
    }

    public String getCol_lat() {
        return col_lat;
    }

    public void setCol_lat(String col_lat) {
        this.col_lat = col_lat;
    }

    public String getCol_lng() {
        return col_lng;
    }

    public void setCol_lng(String col_lng) {
        this.col_lng = col_lng;
    }

    public String getCol_tel() {
        return col_tel;
    }

    public void setCol_tel(String col_tel) {
        this.col_tel = col_tel;
    }
}
